import random

# ฟังก์ชั่นสี่เหลี่ยมจัตุรัส
def square():
    print("โปรแกรมคำนวณหาพื้นที่สี่เหลี่ยมจัตุรัส")
    side = int(input("ใส่ด้าน : "))
    s = side * side
    print(f'ผลลัพธ์ของพื้นที่สี่เหลี่ยมจัตุรัส = {s} ตารางหน่วย\n')

# ฟังก์ชันสี่เหลี่ยมผืนผ้า
def rectangle():
    print("โปรแกรมคำนวณหาพื้นที่สี่เหลี่ยมผืนผ้า")
    wide = int(input("ใส่ความกว้าง : "))
    long = int(input("ใส่ความยาว : "))
    wl = wide * long
    print(f'ผลลัพธ์ของพื้นที่สี่เหลี่ยมผืนผ้า = {wl} ตารางหน่วย\n')

# ฟังก์ชันวงกลม
def circle():
    print("โปรแกรมคำนวณหาพื้นที่วงกลม")
    r = float(input("ใส่รัศมี : "))
    p = 3.14 * (r**2)
    print(f'ผลลัพธ์ของพื้นที่วงกลม = {p} ตารางหน่วย\n')

