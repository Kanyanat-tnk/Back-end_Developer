# ฟังก์ชันคำนวณ
def calculate():
    while True:
        print("\nคุณต้องการคำนวณอะไร?")
        print(" เลือกตอบ : สี่เหลี่ยมจัตุรัส, สี่เหลี่ยมผืนผ้า, วงกลม หรือ 'กลับ' เพื่อกลับไปเมนูหลัก")
        user_input = input("สี่เหลี่ยมจัตุรัส, สี่เหลี่ยมผืนผ้า, วงกลม, กลับ: ")

        # ตรวจสอบคำที่ผู้ใช้ป้อนโดยไม่ใช้ .strip()
        if user_input == "สี่เหลี่ยมจัตุรัส":
            square()
        elif user_input == "สี่เหลี่ยมผืนผ้า":
            rectangle()
        elif user_input == "วงกลม":
            circle()
        elif user_input == "กลับ":
            print("กลับไปยังเมนูหลัก")
            break  # กลับไปที่เมนูหลัก
        else:
            print("กรุณาพิมพ์ชื่อการคำนวณที่ถูกต้อง เช่น 'สี่เหลี่ยมจัตุรัส', 'สี่เหลี่ยมผืนผ้า', หรือ 'วงกลม'")

# ฟังก์ชันเริ่มต้นของโปรแกรม
def start():
    while True:
        print("\nเมนูหลัก:")
        user_input = input("กรุณาพิมพ์คำว่า 'คำนวณ', 'เล่นเกม', หรือ 'ออก' เพื่อจบการทำงานทั้งหมด: ")

        if user_input == "คำนวณ":
            calculate()  # ไปที่เมนูการคำนวณ
        elif user_input == "เล่นเกม":
            game()  # ไปที่เกมเป่ายิงฉุบ
        elif user_input == "ออก":
            print("ขอบคุณที่เข้ามาเล่นจ้า")
            break  # ออกจากโปรแกรม
        else:
            print("กรุณาพิมพ์คำสั่งที่ถูกต้อง เช่น 'คำนวณ', 'เล่นเกม', หรือ 'ออก'")

# เริ่มโปรแกรม
start()
